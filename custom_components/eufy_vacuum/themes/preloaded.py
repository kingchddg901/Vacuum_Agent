def _build_preloaded_theme_entry(
    theme_id: str,
    name: str,
    *,
    colors: dict[str, Any] | None = None,
    tokens: dict[str, Any] | None = None,
    alpha: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Build one preloaded theme entry with canonical token duplication."""
    color_values = dict(colors or {})
    token_values = {
        **color_values,
        **dict(tokens or {}),
    }
    return {
        "id": theme_id,
        "name": name,
        # Bundled themes carry the `core` provenance — the card/gallery Source
        # facet reads this to tell shipped themes apart from user/community ones.
        "source": "core",
        "tokens": token_values,
        "colors": color_values,
        "alpha": dict(alpha or {}),
    }



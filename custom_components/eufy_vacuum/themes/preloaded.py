def ensure_preloaded_theme_library(theme_data: dict[str, Any]) -> None:
    """Ensure the built-in theme library exists without clobbering user themes.

    Called once by ThemeManager.__init__() after storage is loaded so
    the per-call read path stays fast.
    """
    library = theme_data.setdefault("library", {})
    if not isinstance(library, dict):
        library = {}
        theme_data["library"] = library

    for spec in PRELOADED_THEME_SPECS:
        theme_id = spec["id"]
        if theme_id in library:
            # Migration: a bundled theme seeded by an older version (pre-`source`)
            # gets its provenance backfilled so the Source facet sees it as core.
            # Only OUR entries are touched — a user theme's provenance is unknown
            # and is left alone (new ones are stamped at save/import time).
            existing = library[theme_id]
            if isinstance(existing, dict):
                existing.setdefault("source", "core")
            continue
        library[theme_id] = _build_preloaded_theme_entry(
            theme_id,
            spec["name"],
            colors=spec.get("colors"),
            tokens=spec.get("tokens"),
            alpha=spec.get("alpha"),
        )

    default_theme_id = str(theme_data.get("default_theme_id") or "").strip()
    if not default_theme_id or default_theme_id not in library:
        theme_data["default_theme_id"] = "theme_follow_ha"
